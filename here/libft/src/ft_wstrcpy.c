#include "libft.h"

void	ft_wstrcpy(wchar_t *dest, const wchar_t *src)
{
	int		i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
}
