#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*s2;
	int		i;

	i = 0;
	s2 = ft_strdup(s);
	while (s2[i] != '\0')
	{
		s2[i] = f(i, s2[i]);
		i++;
	}
	return (s2);
}
