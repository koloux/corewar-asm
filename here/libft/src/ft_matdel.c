#include "libft.h"

void	ft_matdel(t_matrix **m)
{
	if (m == NULL || *m == NULL)
		return ;
	while (((*m)->y)-- > 0)
		free(((*m)->m)[(*m)->y]);
	free((*m)->m);
	free(*m);
	*m = NULL;
}
