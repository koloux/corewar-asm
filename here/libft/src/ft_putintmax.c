#include "libft.h"

void	ft_putintmax(intmax_t n)
{
	if (n < 0)
		ft_putchar('-');
	if (n > 0)
		n = -n;
	if (n <= -10)
	{
		ft_putintmax(-(n / 10));
		ft_putintmax((10 - (n % 10)) % 10);
	}
	else
		ft_putchar(-n + '0');
}
