#include "libft.h"

void	ft_putintmaxns(intmax_t n)
{
	if (n > 0)
		n = -n;
	if (n <= -10)
	{
		ft_putintmaxns(n / 10);
		ft_putintmaxns((10 - (n % 10)) % 10);
	}
	else
		ft_putchar(-n + '0');
}
