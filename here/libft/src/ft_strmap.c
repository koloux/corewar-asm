#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char	*s2;
	int		i;

	s2 = ft_strdup(s);
	i = 0;
	while (s2[i] != '\0')
	{
		s2[i] = f(s2[i]);
		i++;
	}
	return (s2);
}
