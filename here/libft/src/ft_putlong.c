#include "libft.h"

void	ft_putlong(long n)
{
	if (n < 0)
		ft_putchar('-');
	if (n > 0)
		n = -n;
	if (n <= -10)
		ft_putlong(-(n / 10));
	ft_putchar((10 - (n % 10)) % 10 + '0');
}
